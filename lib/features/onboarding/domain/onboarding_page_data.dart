class OnboardingPageData {
  /// path to the asset
  final String picturePath;

  /// title
  final String title;

  /// subtitle
  final String subtitle;

  /// constructor
  OnboardingPageData({
    required this.picturePath,
    required this.title,
    required this.subtitle,
  });
}
